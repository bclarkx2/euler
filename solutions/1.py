#! /usr/bin/env python3

###############################################################################
# Imports                                                                     #
###############################################################################

import argparse


###############################################################################
# Constants                                                                   #
###############################################################################

UPPER_LIMIT = 1000


###############################################################################
# Problem                                                                     #
###############################################################################

def threeFiveMultiplesSum_itr(upper_limit):

    total = 0

    for num in range(upper_limit):
        if num % 5 == 0 or num % 3 == 0:
            total += num

    return total


###############################################################################
# Helper functions                                                                          #
###############################################################################

def get_args():
    parser = argparse.ArgumentParser()
    parser.add_argument("--limit",
                        type=int,
                        default=UPPER_LIMIT)
    return parser.parse_args()


###############################################################################
# Main script                                                                 #
###############################################################################

def main():

    args = get_args()

    answer = threeFiveMultiplesSum_itr(args.limit)

    print(answer)


if __name__ == '__main__':
    main()
