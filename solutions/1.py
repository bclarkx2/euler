#! /usr/bin/env python3

###############################################################################
# Imports                                                                     #
###############################################################################

import argparse


###############################################################################
# Constants                                                                   #
###############################################################################

UPPER_LIMIT = 1000


###############################################################################
# Problem                                                                     #
###############################################################################

def threeFiveMultiplesSum_itr(upper_limit):

    total = 0

    for num in range(1, upper_limit + 1, 1):
        print(num)

    return total


###############################################################################
# Helper functions                                                            #
###############################################################################

def get_args():
    parser = argparse.ArgumentParser()
    return parser.parse_args()


###############################################################################
# Main script                                                                 #
###############################################################################

def main():

    args = get_args()

    answer = threeFiveMultiplesSum_itr(UPPER_LIMIT)

    print(answer)


if __name__ == '__main__':
    main()
