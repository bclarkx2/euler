#! /usr/bin/env python3

###############################################################################
# Imports                                                                     #
###############################################################################

import argparse


###############################################################################
# Constants                                                                   #
###############################################################################

SUM = 1000


###############################################################################
# Problem                                                                     #
###############################################################################

def pyth_triple_product(triple_sum):

    for a in range(1, triple_sum - 1):
        for b in range(a + 1, triple_sum // 2):
            c = triple_sum - a - b

            if is_triple(a, b, c):
                return a * b * c


def is_triple(a, b, c):
    return a ** 2 + b ** 2 == c ** 2


###############################################################################
# Helper functions                                                            #
###############################################################################

def get_args():
    parser = argparse.ArgumentParser()
    parser.add_argument("--sum",
                        type=int,
                        default=SUM)
    return parser.parse_args()


###############################################################################
# Main script                                                                 #
###############################################################################

def main():

    args = get_args()

    print(pyth_triple_product(args.sum))


if __name__ == '__main__':
    main()
