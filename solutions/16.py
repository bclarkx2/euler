#! /usr/bin/env python3

###############################################################################
# Imports                                                                     #
###############################################################################

import argparse
from common import all_digits


###############################################################################
# Constants                                                                   #
###############################################################################

POWER = 1000


###############################################################################
# Problem                                                                     #
###############################################################################

def sum_of_power_digits(power):

    num = pow(2, power)
    return sum(all_digits(num))


###############################################################################
# Helper functions                                                            #
###############################################################################

def get_args():
    parser = argparse.ArgumentParser()
    parser.add_argument("--power",
                        type=int,
                        default=POWER)
    return parser.parse_args()


###############################################################################
# Main script                                                                 #
###############################################################################

def main():

    args = get_args()

    print(sum_of_power_digits(args.power))


if __name__ == '__main__':
    main()
