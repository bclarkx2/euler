from common import Euler, all_digits
from math import factorial


###############################################################################
# Problem                                                                     #
###############################################################################

class Problem20(Euler):

    def __init__(self):
        super().__init__("num", 100)

    def solve(self):
        return sum(all_digits(factorial(self.num)))


###############################################################################
# Main                                                                        #
###############################################################################

if __name__ == '__main__':
    Problem20().out()
