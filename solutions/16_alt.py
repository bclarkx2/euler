from common import Euler, all_digits


###############################################################################
# Problem                                                                     #
###############################################################################

class Problem16(Euler):

    def __init__(self):
        super().__init__("power", 1000)

    def solve(self):
        num = pow(2, self.power)
        return sum(all_digits(num))


###############################################################################
# Main                                                                        #
###############################################################################

if __name__ == '__main__':
    Problem16().out()
